# include <stdio.h>

int main(void)
{
	int a[5] = {1, 2, 3, 4, 5};
	//a是数组的名字，5是数组中元素(变量)个数，5个元素分别用a[0] a[1] a[2] a[3] a[4]
	int i;

	for (i=0; i<5; ++i)
		printf("%d\n", a[i] );   //a[i]标识第i+1的元素 

	return 0;
}
/*
	在VC++6.0中的输出结果是：
--------------------------
			1
			2
			3
			4
			5
--------------------------
*/