# include <stdio.h>

int main(void)
{
	int * p;			//p是变量名；int * 表示表示p变量存放的是int类型变量的地址
						
	int i = 3;
	int j;
	p = &i;  
			/*
				1. p保存了i的地址, 因此p指向i
				2. p不是i，i也不是p，更准确的说： 修改p的值不影响i的值，修改i的值也不会影响p的值
				3. 如果一个指针变量指向了某个普通变量, 则
					*指针变量  就完全等同于  普通变量
			   例子:	
					如果p是个指针变量，并且p存放了普通变量i的地址
					则p指向了普通变量i
					*p 	就完全等同于  i
					或者说:  在所有出现*p的地方都可以替换成i
							 在所有出现i的地方都可以替换成*p				
			
			        *p 就是以p的内容为地址的变量
			*/
    j = *p;
	printf("i = %d, j = %d, *p = %d\n", i, j, *p);

	return 0;
}